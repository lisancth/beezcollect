-- 【自动收藏】玩家拥有/阅读书籍物品时记入 character_ezcollection_books 并下发
_G.EzAutoAddBook = function(player, bookID)
    if not player or not bookID or not ServerBookDictionary[bookID] then return end
    local guidLow = player:GetGUIDLow()
    local query = CharDBQuery("SELECT 1 FROM character_ezcollection_books WHERE guid = " .. guidLow .. " AND bookID = " .. bookID)
    if not query then
        CharDBExecute("INSERT INTO character_ezcollection_books (guid, bookID, unlockTime) VALUES (" .. guidLow .. ", " .. bookID .. ", " .. os.time() .. ")")
        -- ADD:BOOK 不带 END (客户端 AddList 对每段 tonumber, END会崩)
        player:SendAddonMessage(AddonsPrefix, "ADD:BOOK:" .. bookID, 7, player)
        player:SendBroadcastMessage("|cFF00B0E8[图书馆] 新书籍已收录入库！|r")
    end
end
local AutoAddBook = _G.EzAutoAddBook

-- 【全局】下发书籍总目录。登录时调用，也供 ezCollectionsServer 在 LIST:DATA:BOOKS 请求时调用。
-- 每本书单独发一条(规避~255字节上限)且不带END，最后单独发一条纯END收尾。
_G.EzSendBookCatalog = function(player)
    if not player then return end
    player:SendAddonMessage(AddonsPrefix, "COLLECTIONS:BOOK:END", 7, player)
    for bookID, bookData in pairs(ServerBookDictionary) do
        player:SendAddonMessage(AddonsPrefix, "LIST:DATA:BOOKS:" .. bookID .. "=" .. bookData, 7, player)
    end
    player:SendAddonMessage(AddonsPrefix, "LIST:DATA:BOOKS:END", 7, player)
end

-- 【全局】下发该玩家【已收藏】书籍列表(从数据库读，永久记录，与背包无关)。
-- 登录时调用，也供 ezCollectionsServer 在客户端请求 LIST:BOOK 时调用(reload后重拉，避免变灰)。
-- 收藏是永久的：删书/存仓库都不影响，因为读的是数据库 character_ezcollection_books。
_G.EzSendOwnedBooks = function(player)
    if not player then return end
    local guidLow = player:GetGUIDLow()
    local query = CharDBQuery("SELECT bookID FROM character_ezcollection_books WHERE guid = " .. guidLow)
    local bookList = {"LIST", "BOOK"}
    if query then
        repeat table.insert(bookList, tostring(query:GetUInt32(0))) until not query:NextRow()
    end
    table.insert(bookList, "END")
    player:SendAddonMessage(AddonsPrefix, table.concat(bookList, ":"), 7, player)
end

-- 【登录】机器人安静退出 + 下发目录 + 扫描已拥有书籍 + 延迟发已收藏清单
local function OnPlayerLogin(event, player)
    local isBot = false
    pcall(function() if player:IsBot() then isBot = true end end)
    pcall(function()
        local ip = player:GetPlayerIP()
        if not ip or ip == "" or ip == "0.0.0.0" then isBot = true end
    end)
    if isBot then return end

    -- 1. 发书籍大辞典
    _G.EzSendBookCatalog(player)

    -- 2. 扫描背包: 拥有的书籍物品自动收藏
    for bookID, _ in pairs(ServerBookDictionary) do
        if player:HasItem(bookID) then
            AutoAddBook(player, bookID)
        end
    end

    -- 3. 延迟发送已收藏清单 (给数据库写入留时间)
    local fullGuid = player:GetGUID()
    CreateLuaEvent(function(eventId, delay, repeats)
        local p = GetPlayerByGUID(fullGuid)
        if p then _G.EzSendOwnedBooks(p) end
    end, 2000, 1)
end

-- 【拾取/阅读触发】玩家获得书籍物品时自动收藏
local function OnPlayerLoot(event, player, itemID, count) AutoAddBook(player, itemID) end
local function OnQuestReward(event, player, itemID, count) AutoAddBook(player, itemID) end

EzAutoAddBook = AutoAddBook

-- 【GM命令】.additem 书籍 时自动收藏
local function OnPlayerCommand(event, player, command)
    if not command then return end
    if string.find(string.lower(command), "^additem") then
        local bookID = tonumber(string.match(command, "%d+"))
        if bookID then
            CreateLuaEvent(function()
                if player:HasItem(bookID) then AutoAddBook(player, bookID) end
            end, 500, 1)
        end
    end
end

-- 【聊天命令】#addbook 手动收藏
local function OnPlayerChat(event, player, msg, type, lang)
    if not msg then return false end
    if string.sub(string.lower(msg), 1, 8) == "#addbook" then
        local bookID = tonumber(string.match(msg, "%d+"))
        if bookID then AutoAddBook(player, bookID) end
        return false
    end
end

