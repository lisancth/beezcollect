-- =======================================================================
-- 高等精灵(race=14)出生点改到暴风城内
-- 日期: 2026-06-11
-- 参考: newRaceSources 的高等精灵出生点设置
-- 所在库: acore_world (playercreateinfo 表)
--
-- 说明:
--   原出生点: 艾尔文森林新手村 (zone=12, -8949.95, -132.493, 83.5312)
--   新出生点: 暴风城内英雄谷一带 (zone=1,  -8874.52, 1053.25, 104.8, 朝向2.4)
--   死亡骑士(class=6)保持死亡之地(map=609)不变
--
-- 注: 高等精灵已是联盟阵营(源码 SharedDefines.h 已把 RACE_HIGH_ELF 加进
--     RACEMASK_ALLIANCE),自动继承联盟/暴风城所有任务,无需单独任务链。
--     出生点改动只对【新建角色】生效,现有角色不受影响。
-- =======================================================================

UPDATE `playercreateinfo`
SET `map` = 0, `zone` = 1,
    `position_x` = -8874.52, `position_y` = 1053.25, `position_z` = 104.8, `orientation` = 2.4
WHERE `race` = 14 AND `class` != 6;


